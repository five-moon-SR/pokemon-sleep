"""メインスキルの効果量テーブルと、エナジー相当への換算。

データソース: 貼り付けデータ集/メインスキル/*.txt（Wiki Ver.3.2.0前後）。
仕様詳細: docs/eval_context/main_skill_notes.md。

評価チェッカー（utils/evaluator.py）から呼ばれ、1回発動あたりの
「エナジー相当」期待値を返す。同じカテゴリ内の派生スキルは
親カテゴリの効果量で代用する（main_skill_notes.md の方針通り）。
"""

from __future__ import annotations

# ---------------------------------------------------------------------------
# 効果量テーブル（Lv別、生の値）
# ---------------------------------------------------------------------------
# キーは main_skill.json の "category" と一致。Lv は 1-indexed の dict。
# 派生スキル（ナイトメア・きのみジュース・つきのひかり等）はこのテーブルを共有。
