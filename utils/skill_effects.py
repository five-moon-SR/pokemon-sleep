# ---------------------------------------------------------------------------
# ランダム版補正（S/ランダム を持つカテゴリのみ）
# ---------------------------------------------------------------------------
# 効果量レンジ = 固定版 × [0.5, 2.0]。期待値（一様分布）= 固定版 × 1.25 (Lv1-7)
# ゆめのかけら Lv8 のみ レンジ [0.46, 1.84] → 期待値 × 1.15。
# main_skill.json には variant 情報がないため、現状は「ランダム版を持つポケモンを名前で判定」する。
# v1 簡易実装: 派生スキル名で判別（ノート由来）
RANDOM_VARIANT_SKILL_NAMES: set[str] = {
    # エナジーチャージS/ランダム を持つポケのスキル
    "エナジーチャージS",  # ゴルダック・オーダイル等が S/ランダム を持つ
    # ※ main_skill.json で variant=S/ランダム を区別できるようになったら、
    #    ここではなくレコード側で判定する。現状は「該当カテゴリ全部 ×1.25 平均」で簡易処理。
}

# 暫定: ランダム版の期待値補正は default で適用しない（固定版のテーブルをそのまま使う）。
# 理由: main_skill.json に variant がない／全カテゴリで Lv7 max のため Lv8 補正も発動しない。
# 将来 main_skill.json に variant 列を足したら ENABLE_RANDOM_AVERAGING を True に。
ENABLE_RANDOM_AVERAGING: bool = False
RANDOM_AVG_FACTOR_DEFAULT: float = 1.25  # Lv1-7
RANDOM_AVG_FACTOR_LV8: float = 1.15      # ゆめかけら Lv8 のみ


# ---------------------------------------------------------------------------
# アクセサ
# ---------------------------------------------------------------------------
def get_skill_effect_amount(category: str, lv: int) -> float | None:
    """カテゴリと Lv から効果量（生の値）を返す。テーブル未収録なら None。

    Lv はテーブルの max を超えたら頭打ち。1未満は 1 にクリップ。
    """
    table = SKILL_EFFECT_TABLE.get(category)
    if not table:
        return None
    max_lv = max(table.keys())
    lv = max(1, min(int(lv), max_lv))
    return float(table[lv])


def get_skill_name_energy(skill_name: str | None, lv: int) -> float | None:
    """派生スキル固有テーブル（エナジー直値）から1発動あたりのエナジーを返す。未収録は None。"""
    if not skill_name:
        return None
    table = SKILL_NAME_EFFECT_TABLE.get(skill_name)
    if not table:
        return None
    max_lv = max(table.keys())
    lv = max(1, min(int(lv), max_lv))
    return float(table[lv])


def get_skill_energy_per_activation(
    category: str, lv: int, *, skill_name: str | None = None
) -> float | None:
    """カテゴリと Lv から、1回発動あたりの「エナジー相当」期待値を返す。

    skill_name が派生スキル固有テーブルにあればそちらを優先（複合効果を換算済みの直値）。
    テーブル未収録カテゴリ（ゆびをふる/スキルコピー 等）は None。
    呼び出し元で MAIN_SKILL_CATEGORY_COEF にフォールバックする想定。
    """
    name_energy = get_skill_name_energy(skill_name, lv)
    if name_energy is not None:
        return name_energy
    raw = get_skill_effect_amount(category, lv)
    if raw is None:
        return None
    factor = ENERGY_PER_UNIT.get(category, 1.0)
    energy = raw * factor
    if ENABLE_RANDOM_AVERAGING and category in RANDOM_VARIANT_SKILL_NAMES:
        avg = RANDOM_AVG_FACTOR_LV8 if lv >= 8 else RANDOM_AVG_FACTOR_DEFAULT
        energy *= avg
    return energy


def get_skill_max_lv(category: str) -> int | None:
    """テーブルが持つ最大 Lv。未収録なら None。"""
    table = SKILL_EFFECT_TABLE.get(category)
    if not table:
        return None
    return max(table.keys())
