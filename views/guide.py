st.subheader("① 今週の前提")
st.markdown(
    """
**今週どんな条件でプレイするか** を最初に設定します。

- **フィールド**：好物のきのみが反映され、きのみ期待値の倍率（×2）に効きます
  - ワカクサ本島など「ランダム3種」フィールドは今週の3種を multiselect で選択
- **作る料理カテゴリ**：候補レシピ・つなぎ料理候補のフィルタになります
- **候補レシピ**：必要な食材が ② のスコアに反映されます
- **🎯 役割×目標数スライダー**：5役割（💚回復／⚡供給／🍳鍋容量UP／🍓きのみ枠／🥕食材枠）の目標人数
  - **プリセット6種**（バランス／食材寄せ／きのみ全ツッパ／回復多め／鍋拡張型／カスタム）から選んで「📋 適用」でスライダーに初期値投入
- **✨ イベント補正**：きのみエナジー2倍週／料理エナジー2倍週／食材獲得2倍週／全ポケエナジーUP（複数選択可、空＝補正なし週）
"""
)

st.subheader("② 候補ポケモン（役割別）")
st.markdown(
    """
①で目標数 > 0 の役割タブが優先表示されます（全0なら全役割）。

- 各タブで該当役割のポケモンが **スコア順** に表示
- 役割スコアは **性格軸／メインスキルLv／スキル発動系サブスキル／特化／好物倍率／イベント補正** が反映
- 役割対象外のポケモンはそもそもタブに出てこない（例：きのみ枠タブにげんき回復スキル持ちは出ない）
- 「追加」ボタンで編成中5枠に投入
"""
)

st.subheader("③ 編成中のパーティ")
st.markdown(
    """
選んだ5体に対して、自動で各種サマリが計算されます。

- **🤝 team-buff バナー**：おてつだいボーナス装着 N人 → 全員のスピード ×(1 + 0.05N)
- **🍓 きのみ獲得**：1日あたり個数とエナジー（好物倍率込み、フィールドボーナス対応予定）
- **🥕 食材獲得期待値**：性格・サブ・Lv・🎀リボン・team-buff 込みの 1日獲得個数
- **🎯 メインスキル構成 ／ 役割充足度**：目標 vs 現在の進捗バー
- **🍳 レシピ達成進捗**：候補レシピごとに「何日で揃うか」と不足食材を赤警告
- **🍽 主料理＋つなぎ料理提案** ⭐ メイン機能
"""
)

with st.expander("🍽 主料理＋つなぎ料理提案 の詳しい使い方", expanded=True):
    st.markdown(
        """
1. **主料理を1つ選ぶ**：①の候補レシピがあればそこから、無ければカテゴリ絞り込みからの全レシピ
2. **作成可能回数 / 日** と **🔻 律速食材** が表示される
3. **📦 余剰食材リスト**（主料理ペース後の残量）を確認
4. **🍳 つなぎ料理候補**（最大8件）が表示される

候補は **1日あたり期待エナジー降順** にソート：

- 🍳 = **余剰だけで作れる**（毎日コンスタントに作れる）
- 🥄 = **ストック前提**（先週の残りなどで補えば作れる）

必要食材・不足食材は **食材アイコン＋×N チップ** で表示されます。

**🚫 最小1個エナジー** フィルタ：
潜在エナジーがしきい値未満の料理は候補から除外できます。
「単一食材で量産できるけど1個あたりのエナジーが低い料理」を弾きたい時に便利。
0 で全候補表示、4000 などに上げると低エナジー料理がスパッと消えます。

**バッジの意味**：
- ⚠ 律速食材使用 — 主料理の律速食材を消費する候補（×0.3 ペナルティで下位に）
- ❌ 鍋超過 — `pot_capacity`（プロフィールの鍋容量）を超える料理（×0.5 ペナルティ）
"""
    )

