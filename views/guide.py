"""使い方ガイド。"""

from __future__ import annotations

import streamlit as st


st.title("📖 使い方ガイド")
st.caption("週初めの攻略プラン作成と、個体管理の使い分けをまとめています。")

st.header("1. 基本の流れ")
st.markdown(
    """
1. **「📝 個体登録」**で所持ポケモンを登録する
2. **「📦 所持ポケデータ」**で現在評価と将来性を確認する
3. 週初めに**「🧭 攻略プラン」**を開く
4. 今週の**料理カテゴリ → フィールド**の順に選ぶ
5. 保存済みの定番プランを開くか、自動提案から主料理と固定5体を作る
6. 今週だけのイベント・ランダム好物きのみ・持越し食材を設定する
7. **「📌 今週のプランに設定」**するとホームへ見通しが表示される
"""
)
st.info("日常運用は「ホーム」と「攻略プラン」の2画面で完結します。")

st.header("2. 攻略プラン")
st.markdown(
    """
同じフィールドでも、カレー・サラダ・デザートでは必要な5体が変わります。
そのため、**フィールド×料理カテゴリごとに定番プランを1つ**保存します。

- **主料理**：週を通して狙う1品
- **固定5体**：基本的に週を通して使うメンバー
- **つなぎ料理**：主料理で余る食材から自動提案
- **今週だけの条件**：イベント補正、ランダム好物きのみ、持越し食材
"""
)

with st.expander("✨ 自動提案の見方", expanded=True):
    st.markdown(
        """
カテゴリ内の全料理と所持個体から候補を絞り、上位案を7日間シミュレーションします。

- **3食安定度**：21回の食事機会のうち、主料理を作れる割合
- **週期待エナジー**：主料理・きのみ・直接スキルの合計
- **ヒーラーあり／なし**：げんきオール役を採用した案も比較
- **鍋拡張が必要**：通常鍋では作れず、料理パワーアップ発動後だけ作れる食事数

「採用」で主料理と5体が編集欄へ入り、個体を手動で入れ替えて再計算できます。
"""
    )

with st.expander("📋 今週の見通し"):
    st.markdown(
        """
- 主料理の1日作成回数と3食安定度
- 食材別の供給量と律速食材
- 鍋容量UP・料理チャンスの発動期待
- げんきオールによるチーム稼働率の上昇
- 持越し食材あり／在庫ゼロの差
"""
    )

with st.expander("🌱 育成・捕獲"):
    st.markdown(
        """
- 所持個体をLv30・60へ育てた時の安定度と週エナジー改善
- 未所持の最終進化AAA個体を各枠へ入れた時の改善
- 誰と交代し、どの律速食材を埋められるか

捕獲候補は一般的な強さではなく、**現在の主料理をどれだけ安定させるか**を中心に並びます。
"""
    )

st.header("3. 各ページの役割")
st.markdown(
    """
- **🏠 ホーム**：今週の固定5体、主料理、安定度、律速、次の育成・捕獲候補
- **🧭 攻略プラン**：定番プランの作成・分析・今週設定
- **📦 所持ポケデータ**：個体の検索、現在評価、将来性
- **📝 個体登録**：新しい個体の登録
- **🔧 個体強化・進化**：Lv、進化、スキル、リボンの更新
- **✏ 登録情報の修正**：登録ミスの修正
- **📚 全ポケデータ／🗂 データ集**：マスター情報の確認
"""
)

st.header("4. 計算上の注意")
st.markdown(
    """
- 標準評価は**月曜開始時の食材在庫ゼロ**。実運用は「今週の持越し食材」で比較できます
- 料理チャンスの蓄積と大成功時リセットは期待値で近似しています
- げんきオールは発動期待回数からチーム全体の稼働増分を計算します
- げんきエール・自己回復は精密シミュレーション対象外で、既存の近似評価を使います
- 最大所持数による夜間の取りこぼしや、手動交代運用は未反映です
- 新ポケモン・新料理・仕様変更時はマスターデータの更新が必要です
"""
)
